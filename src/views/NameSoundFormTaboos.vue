                      <li><span class="highlight">苦(kǔ, bitter)</span>: Avoid characters that sound like "苦", such as "库, 酷"</li>
                      <li><span class="highlight">败(bài, defeat)</span>: Avoid characters that sound like "败", such as "拜, 稗"</li>
