                <li><strong>上下结构：</strong>如"字、安、室"等，上下两部分排列</li>
                <li><strong>半包围结构：</strong>如"问、同、闻"等，一部分包围另一部分三面</li>
                <li><strong>全包围结构：</strong>如"国、围、圆"等，一部分包围另一部分四面</li>
