                <li><strong>确定名字主题：</strong>基于对孩子的期望和家族传统确定名字的主题和核心寓意</li>
                <li><strong>字库初选：</strong>根据主题和寓意选择一批候选字，注意字义的丰富性和积极性</li>
                <li><strong>结构搭配：</strong>考虑与姓氏的结构搭配，筛选出结构平衡的组合</li>
