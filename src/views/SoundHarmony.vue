              <p>普通话有四种基本声调和一个轻声：</p>
              <ul>
                <li><strong>第一声（阴平）：</strong>高而平，如"诗、天、言"（音高曲线：➖）</li>
                <li><strong>第二声（阳平）：</strong>由中到高，如"时、田、颜"（音高曲线：➗）</li>
                <li><strong>第三声（上声）：</strong>先降后升，如"史、展、彦"（音高曲线：✓）</li>
                <li><strong>第四声（去声）：</strong>由高至低，如"是、典、焱"（音高曲线：➘）</li>
                <li><strong>轻声：</strong>短促且轻，如"的、了、吗"（音高取决于前一个音节）</li>
