                <li><strong>异韵互补：</strong>不同韵母的搭配，如"博雅"（o-a），增加变化的同时保持和谐</li>
                <li><strong>避免重复单调：</strong>避免使用过于相似的韵母，造成音韵单调，如"明星"的鼻韵母</li>
                <li><strong>和谐过渡：</strong>相邻字的韵母应当有自然的过渡，避免发音跳跃感过强</li>
              </ul>
